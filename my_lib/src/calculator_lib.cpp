#include "calculator_lib.h"

Calculator_lib::Calculator_lib()
{
}

double Calculator_lib::add(double num1, double num2)
{
    return num1+num2;
}

double Calculator_lib::subtract(double num1, double num2)
{
    return num1-num2;
}

double Calculator_lib::multiply(double num1, double num2)
{
    return num1*num2;
}

double Calculator_lib::divide(double num1, double num2)
{
    return num1/num2;
}
